#!/usr/bin/env node

import fs from 'node:fs'
import path from 'node:path'
import os from 'node:os'

const home = os.homedir()
const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..')
const outputFile = path.join(repoRoot, 'src', 'data', 'skills.generated.ts')

const sources = [
  {
    id: 'claude',
    label: 'Claude',
    type: 'local',
    root: path.join(home, '.claude', 'skills'),
    includeHidden: false,
  },
  {
    id: 'codex-user',
    label: 'Codex',
    type: 'user',
    root: path.join(home, '.codex', 'skills'),
    includeHidden: false,
  },
  {
    id: 'codex-system',
    label: 'Codex System',
    type: 'system',
    root: path.join(home, '.codex', 'skills', '.system'),
    includeHidden: true,
  },
]

function readDirSafe(dir) {
  try {
    return fs.readdirSync(dir, { withFileTypes: true })
  } catch {
    return []
  }
}

function readFileSafe(file) {
  try {
    return fs.readFileSync(file, 'utf8')
  } catch {
    return ''
  }
}

function parseFrontmatter(markdown) {
  const match = markdown.match(/^---\n([\s\S]*?)\n---\n?/)
  if (!match) return {}

  const lines = match[1].split('\n')
  const result = {}
  let currentKey = null
  let currentValue = []

  const flush = () => {
    if (!currentKey) return
    result[currentKey] = currentValue.join('\n').trim()
  }

  for (const line of lines) {
    const field = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/)
    if (field) {
      flush()
      currentKey = field[1]
      currentValue = [field[2]]
      continue
    }

    if (currentKey) currentValue.push(line)
  }

  flush()
  return result
}

function normalizeDescription(value) {
  return stripWrappingQuotes(
    value.replace(/^>\s*/, '').replace(/\n\s*/g, ' ').replace(/\s+/g, ' ').trim(),
  )
}

function stripWrappingQuotes(value) {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1)
  }
  return value
}

function listSkillDirs(root, includeHidden) {
  return readDirSafe(root)
    .filter((entry) => entry.isDirectory())
    .filter((entry) => includeHidden || !entry.name.startsWith('.'))
    .map((entry) => path.join(root, entry.name))
    .filter((dir) => fs.existsSync(path.join(dir, 'SKILL.md')))
    .sort((a, b) => a.localeCompare(b))
}

function buildSkillRecord(source, dir) {
  const skillFile = path.join(dir, 'SKILL.md')
  const markdown = readFileSafe(skillFile)
  const frontmatter = parseFrontmatter(markdown)
  const referencesDir = path.join(dir, 'references')
  const scriptsDir = path.join(dir, 'scripts')
  const agentsDir = path.join(dir, 'agents')

  const referencesCount = readDirSafe(referencesDir).filter((entry) => entry.isFile()).length
  const scriptsCount = readDirSafe(scriptsDir).filter((entry) => entry.isFile()).length
  const agentsCount = readDirSafe(agentsDir).filter((entry) => entry.isFile()).length

  return {
    name: stripWrappingQuotes(frontmatter.name || path.basename(dir)),
    description: normalizeDescription(frontmatter.description || ''),
    sourceId: source.id,
    sourceLabel: source.label,
    sourceType: source.type,
    folder: path.basename(dir),
    skillPath: skillFile.replace(home, '~'),
    referencesCount,
    scriptsCount,
    agentsCount,
  }
}

function generate() {
  const sourceRecords = sources.map((source) => {
    const skills = listSkillDirs(source.root, source.includeHidden).map((dir) => buildSkillRecord(source, dir))
    return {
      id: source.id,
      label: source.label,
      type: source.type,
      root: source.root.replace(home, '~'),
      count: skills.length,
      skills,
    }
  })

  const banner =
    '// This file is generated by scripts/generate-skills-data.mjs. Do not edit by hand.\n'
  const content = `${banner}
export type SkillRecord = {
  name: string
  description: string
  sourceId: string
  sourceLabel: string
  sourceType: 'local' | 'user' | 'system'
  folder: string
  skillPath: string
  referencesCount: number
  scriptsCount: number
  agentsCount: number
}

export type SkillSource = {
  id: string
  label: string
  type: 'local' | 'user' | 'system'
  root: string
  count: number
  skills: SkillRecord[]
}

export const skillSources: SkillSource[] = ${JSON.stringify(sourceRecords, null, 2)} as const

export const allSkills: SkillRecord[] = skillSources.flatMap((source) => source.skills)
`

  fs.writeFileSync(outputFile, content)
  console.log(`generated ${outputFile}`)
}

generate()
